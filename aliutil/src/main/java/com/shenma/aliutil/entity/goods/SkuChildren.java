package com.shenma.aliutil.entity.goods;

public class SkuChildren {
	private Integer cargoNumber;
	private String value;
	private Integer canBookCount;
	private String specId;
	private Long fid;
	private String price;
	private String retailPrice;
	private Integer saleCount;
	public Integer getCargoNumber() {
		return cargoNumber;
	}
	public void setCargoNumber(Integer cargoNumber) {
		this.cargoNumber = cargoNumber;
	}
	public String getValue() {
		return value;
	}
	public void setValue(String value) {
		this.value = value;
	}
	public Integer getCanBookCount() {
		return canBookCount;
	}
	public void setCanBookCount(Integer canBookCount) {
		this.canBookCount = canBookCount;
	}
	public String getSpecId() {
		return specId;
	}
	public void setSpecId(String specId) {
		this.specId = specId;
	}
	public Long getFid() {
		return fid;
	}
	public void setFid(Long fid) {
		this.fid = fid;
	}
	public String getPrice() {
		return price;
	}
	public void setPrice(String price) {
		this.price = price;
	}
	public String getRetailPrice() {
		return retailPrice;
	}
	public void setRetailPrice(String retailPrice) {
		this.retailPrice = retailPrice;
	}
	public Integer getSaleCount() {
		return saleCount;
	}
	public void setSaleCount(Integer saleCount) {
		this.saleCount = saleCount;
	}
	
	
}
